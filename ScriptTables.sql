
DROP TABLE IF EXISTS SCHEDULE CASCADE;
DROP TABLE IF EXISTS STUDENTS_COURSE_GROUP_STUDENTS CASCADE;
DROP TABLE IF EXISTS STUDENTS_COURSE_GROUPS CASCADE;
DROP TABLE IF EXISTS INSTRUCTORS_COURSES CASCADE;
DROP TABLE IF EXISTS LESSONS_SCHEDULE CASCADE;
DROP TABLE IF EXISTS ROOMS CASCADE;
DROP TABLE IF EXISTS COURSES CASCADE;
DROP TABLE IF EXISTS INSTRUCTORS CASCADE;
DROP TABLE IF EXISTS STUDENTS CASCADE;
DROP TABLE IF EXISTS AUDIT_LOG CASCADE;

-- ============================================================================
-- TABLES
-- ============================================================================

CREATE TABLE STUDENTS (
    ID VARCHAR(36) PRIMARY KEY,
    FIRST_NAME VARCHAR(200) NOT NULL,
    LAST_NAME VARCHAR(200) NOT NULL,
    EMAIL VARCHAR(200) NOT NULL UNIQUE,
    PHONE VARCHAR(20),
    COURSE INT NOT NULL CHECK (COURSE >= 1 AND COURSE <= 6),
    EDUCATIONAL_DEGREE VARCHAR(20) NOT NULL,
    SPECIALITY VARCHAR(200) NOT NULL,
    ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE INSTRUCTORS (
    ID VARCHAR(36) PRIMARY KEY,
    FIRST_NAME VARCHAR(200) NOT NULL,
    LAST_NAME VARCHAR(200) NOT NULL,
    EMAIL VARCHAR(200) NOT NULL UNIQUE,
    PHONE VARCHAR(20),
    ACTIVE BOOLEAN DEFAULT TRUE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE COURSES (
    ID VARCHAR(36) PRIMARY KEY,
    COURSE_DISPLAY_SHORT_NAME VARCHAR(36) NOT NULL UNIQUE,
    COURSE_DISPLAY_FULL_NAME VARCHAR(200) NOT NULL,
    COURSE_DESCRIPTION VARCHAR(500),
    LECTURES_NUM INT NOT NULL CHECK (LECTURES_NUM >= 0),
    PRACTICES_NUM INT NOT NULL CHECK (PRACTICES_NUM >= 0),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE ROOMS (
    ID VARCHAR(36) PRIMARY KEY,
    BUILDING VARCHAR(200) NOT NULL,
    FLOOR INT NOT NULL CHECK (FLOOR >= 0),
    NUMBER INT NOT NULL,
    DISPLAY_NAME VARCHAR(200),
    SEATS_NUMBER INT NOT NULL CHECK (SEATS_NUMBER > 0),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(BUILDING, FLOOR, NUMBER)
);

CREATE TABLE LESSONS_SCHEDULE (
    ID INT PRIMARY KEY,
    START_TIME TIME NOT NULL,
    END_TIME TIME NOT NULL,
    CHECK (START_TIME < END_TIME)
);

CREATE TABLE INSTRUCTORS_COURSES (
    INSTRUCTOR_ID VARCHAR(36) NOT NULL,
    COURSE_ID VARCHAR(36) NOT NULL,
    PRIMARY KEY (INSTRUCTOR_ID, COURSE_ID),
    FOREIGN KEY (INSTRUCTOR_ID) REFERENCES INSTRUCTORS(ID) ON DELETE CASCADE,
    FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID) ON DELETE CASCADE
);

CREATE TABLE STUDENTS_COURSE_GROUPS (
    ID VARCHAR(36) PRIMARY KEY,
    COURSE_ID VARCHAR(36) NOT NULL,
    GROUP_NUMBER INT NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID) ON DELETE CASCADE,
    UNIQUE(COURSE_ID, GROUP_NUMBER)
);

CREATE TABLE STUDENTS_COURSE_GROUP_STUDENTS (
    STUDENT_ID VARCHAR(36) NOT NULL,
    GROUP_ID VARCHAR(36) NOT NULL,
    ENROLLED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (STUDENT_ID, GROUP_ID),
    FOREIGN KEY (STUDENT_ID) REFERENCES STUDENTS(ID) ON DELETE CASCADE,
    FOREIGN KEY (GROUP_ID) REFERENCES STUDENTS_COURSE_GROUPS(ID) ON DELETE CASCADE
);

CREATE TABLE SCHEDULE (
    ID SERIAL PRIMARY KEY,
    COURSE_ID VARCHAR(36) NOT NULL,
    INSTRUCTOR_ID VARCHAR(36) NOT NULL,
    STUDENTS_COURSE_GROUP_ID VARCHAR(36) NOT NULL,
    WEEK_DAY VARCHAR(20) NOT NULL CHECK (WEEK_DAY IN ('MONDAY', 'TUESDAY', 'WEDNESDAY', 'THURSDAY', 'FRIDAY', 'SATURDAY', 'SUNDAY')),
    LESSON_SCHEDULE_ID INT NOT NULL,
    ROOM_ID VARCHAR(36) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID) ON DELETE CASCADE,
    FOREIGN KEY (INSTRUCTOR_ID) REFERENCES INSTRUCTORS(ID) ON DELETE CASCADE,
    FOREIGN KEY (STUDENTS_COURSE_GROUP_ID) REFERENCES STUDENTS_COURSE_GROUPS(ID) ON DELETE CASCADE,
    FOREIGN KEY (LESSON_SCHEDULE_ID) REFERENCES LESSONS_SCHEDULE(ID) ON DELETE CASCADE,
    FOREIGN KEY (ROOM_ID) REFERENCES ROOMS(ID) ON DELETE CASCADE
);

CREATE TABLE AUDIT_LOG (
    ID SERIAL PRIMARY KEY,
    TABLE_NAME VARCHAR(50) NOT NULL,
    OPERATION VARCHAR(10) NOT NULL,
    RECORD_ID VARCHAR(36),
    OLD_VALUES JSON,
    NEW_VALUES JSON,
    CHANGED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CHANGED_BY VARCHAR(100)
);

