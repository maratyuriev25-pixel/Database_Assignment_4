CREATE OR REPLACE FUNCTION log_students_changes()
RETURNS TRIGGER AS $$
BEGIN
    IF TG_OP = 'DELETE' THEN
        INSERT INTO AUDIT_LOG (TABLE_NAME, OPERATION, RECORD_ID, OLD_VALUES, CHANGED_BY)
        VALUES ('STUDENTS', TG_OP, OLD.ID, row_to_json(OLD), CURRENT_USER);
        RETURN OLD;
    ELSIF TG_OP = 'UPDATE' THEN
        INSERT INTO AUDIT_LOG (TABLE_NAME, OPERATION, RECORD_ID, OLD_VALUES, NEW_VALUES, CHANGED_BY)
        VALUES ('STUDENTS', TG_OP, NEW.ID, row_to_json(OLD), row_to_json(NEW), CURRENT_USER);
        RETURN NEW;
    ELSIF TG_OP = 'INSERT' THEN
        INSERT INTO AUDIT_LOG (TABLE_NAME, OPERATION, RECORD_ID, NEW_VALUES, CHANGED_BY)
        VALUES ('STUDENTS', TG_OP, NEW.ID, row_to_json(NEW), CURRENT_USER);
        RETURN NEW;
    END IF;
    RETURN NULL;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER students_audit_trigger
AFTER INSERT OR UPDATE OR DELETE ON STUDENTS
FOR EACH ROW
EXECUTE FUNCTION log_students_changes();

CREATE OR REPLACE FUNCTION check_room_capacity()
RETURNS TRIGGER AS $$
DECLARE
    room_capacity INT;
    group_size INT;
BEGIN
    SELECT SEATS_NUMBER INTO room_capacity FROM ROOMS WHERE ID = NEW.ROOM_ID;
    
    SELECT COUNT(*) INTO group_size 
    FROM STUDENTS_COURSE_GROUP_STUDENTS 
    WHERE GROUP_ID = NEW.STUDENTS_COURSE_GROUP_ID;
    
    IF group_size > room_capacity THEN
        RAISE EXCEPTION 'Room capacity % is less than group size %', room_capacity, group_size;
    END IF;
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER schedule_room_capacity_trigger
BEFORE INSERT OR UPDATE ON SCHEDULE
FOR EACH ROW
EXECUTE FUNCTION check_room_capacity();

CREATE OR REPLACE FUNCTION check_instructor_course()
RETURNS TRIGGER AS $$
DECLARE
    teaches BOOLEAN;
BEGIN
    SELECT EXISTS(
        SELECT 1 FROM INSTRUCTORS_COURSES 
        WHERE INSTRUCTOR_ID = NEW.INSTRUCTOR_ID 
        AND COURSE_ID = NEW.COURSE_ID
    ) INTO teaches;
    
    IF NOT teaches THEN
        RAISE EXCEPTION 'Instructor % does not teach course %', NEW.INSTRUCTOR_ID, NEW.COURSE_ID;
    END IF;
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER schedule_instructor_course_trigger
BEFORE INSERT OR UPDATE ON SCHEDULE
FOR EACH ROW
EXECUTE FUNCTION check_instructor_course();
